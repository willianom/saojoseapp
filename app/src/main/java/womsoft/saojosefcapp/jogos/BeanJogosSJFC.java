public class BeanJogosSJFC {

    private String id;
    private String time_mand;
    private String time_visit;
    private String placar_mand;
    private String placar_visit;
    private String data_hora;
    private String local;
    private String campeonato;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime_mand() {
        return time_mand;
    }

    public void setTime_mand(String time_mand) {
        this.time_mand = time_mand;
    }

    public String getTime_visit() {
        return time_visit;
    }

    public void setTime_visit(String time_visit) {
        this.time_visit = time_visit;
    }

    public String getPlacar_mand() {
        return placar_mand;
    }

    public void setPlacar_mand(String placar_mand) {
        this.placar_mand = placar_mand;
    }

    public String getPlacar_visit() {
        return placar_visit;
    }

    public void setPlacar_visit(String placar_visit) {
        this.placar_visit = placar_visit;
    }

    public String getData_hora() {
        return data_hora;
    }

    public void setData_hora(String data_hora) {
        this.data_hora = data_hora;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getCampeonato() {
        return campeonato;
    }

    public void setCampeonato(String campeonato) {
        this.campeonato = campeonato;
    }


}
