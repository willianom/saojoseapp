public class BeanElencoSJFC {

    private String id_jogador;
    private String nome_jogador;
    private String apelido_jogador;
    private String idade_jogador;
    private String posicao_jogador;


    public String getId_jogador() {
        return id_jogador;
    }

    public void setId_jogador(String id_jogador) {
        this.id_jogador = id_jogador;
    }

    public String getNome_jogador() {
        return nome_jogador;
    }

    public void setNome_jogador(String nome_jogador) {
        this.nome_jogador = nome_jogador;
    }

    public String getApelido_jogador() {
        return apelido_jogador;
    }

    public void setApelido_jogador(String apelido_jogador) {
        this.apelido_jogador = apelido_jogador;
    }

    public String getIdade_jogador() {
        return idade_jogador;
    }

    public void setIdade_jogador(String idade_jogador) {
        this.idade_jogador = idade_jogador;
    }

    public String getPosicao_jogador() {
        return posicao_jogador;
    }

    public void setPosicao_jogador(String posicao_jogador) {
        this.posicao_jogador = posicao_jogador;
    }

}
